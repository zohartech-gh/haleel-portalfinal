async function main() {
  await warmUp();
  const subject = await prisma.subject.findFirst({
    where: { name: "Biology", level: "SHS" },
  });
  if (!subject) {
    console.log("Biology not found!");
    return;
  }
  let total = 0;
  for (let i = 0; i < biologyQuestions.length; i += 50) {
    const batch = biologyQuestions.slice(i, i + 50);
    const result = await prisma.question.createMany({
      data: batch.map((q) => ({
        subjectId: subject.id,
        level: "SHS",
        examType: "WASSCE",
        topic: q.topic,
        year: q.year,
        questionText: q.questionText,
        optionA: q.optionA,
        optionB: q.optionB,
        optionC: q.optionC,
        optionD: q.optionD,
        correctOption: q.correctOption,
        explanation: q.explanation,
        difficulty: q.difficulty as any,
      })),
      skipDuplicates: true,
    });
    total += result.count;
    console.log(`  Batch ${Math.floor(i / 50) + 1}: ${result.count} created`);
  }
  console.log(`Biology: ${total}/200 created`);
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
